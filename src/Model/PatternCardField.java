package Model;

public class PatternCardField {
	final private String color;
	final private int number;
	
	
	public PatternCardField(String color, int number) {
		this.color = color;
		this.number = number;
	}


	public String getColor() {
		return color;
	}
	

	public int getNumber() {
		return number;
	}
	
}
